package uk.ac.rhul.cs2800.model;

/**
 * A class represents a move rate.
 */
public class Rate {
  int score;

  public Rate() {

  }

  public Rate(int score) {
    this.score = score;
  }

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }


}
